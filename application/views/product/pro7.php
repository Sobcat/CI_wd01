<h2>风冷／水冷</h2>
<img src="/pinrui/assets/images/pro/007.png" alt="">

<p>说明：</p>
<p>冷却系统在整个铝挤压型材生产过程中起到至关重要的作用，部分决定了型材的成型质量、后期加工的变形程度。</p>
<p>冷却系统有风冷系、在线水冷系统；本公司冷却系统实现数字化、PLC数控面板控制：
任意启闭、切换每一列淋头或雾头，并可随时调整其水量、水压，此功能对解决截面复杂壁厚特殊的型材的冷却一致性起决定作用。</p>
<p>大功率变频调速水泵供水系统，能满足喷淋（雾）总水的无级调速的需求，并达到最大限度的节能效果。</p>
<p>水冷风冷调节采用数字化控制，先进的软件系统不仅带来操作的简使快捷，而且为达到理想的工艺指标提供有力的保障。</p>
