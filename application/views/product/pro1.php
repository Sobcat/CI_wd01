<h2>毛毡皮带式/链式皮带式/同步带式冷床</h2>
<img src="/pinrui/assets/images/pro/001.png" alt="">

<p>说明：</p>
<p>1．由滑出台、冷却台、调直台、储料台及成品台等组成</p>
<p>2．可选配出料水冷系统，牵引机与中断锯，拉直机或成品锯等机械</p>
<p>3．冷床的传动皮带根据挤压需求采用皮带、同步皮带以及链条皮带</p>
<p>4．具体规格尺寸根据挤压机确定</p>
<p>主要性能</p>
<p>1．PLC控制、触摸屏显示与操作</p>
<p>2．成品制作与控制工艺、输送过程中无擦伤、碰伤等情况，提高成品率</p>
<p>3．附带脚踏开关，便于各种操作</p>
<p>4．选用高质量的毛毡，延长使用寿命减少维护</p>
<p>5．皮带更换采用分组独立，便于维护与更换</p>