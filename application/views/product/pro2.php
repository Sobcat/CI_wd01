<h2>平移式冷床</h2>
<img src="/pinrui/assets/images/pro/002.png" alt="">

<p>技术特点：</p>
<p>1、将结构相同的两套步进梁机构交错组装在一起，分别用抬升液压油缸和移动液压油缸通过连杆与步进梁连接，实现型材在冷床上的无抬升平移运动。</p>
<p>2、适合冷却面积大、承载载荷重、异型、多孔的铝挤压型材使用。</p>
<p>说明：</p>
<p>1．由滑出台、移料台、冷却台、调直台、储料台、成品切割台及成品装料台等组成</p>
<p>2．可选配出料水冷系统，牵引机与中断锯，拉直机或成品锯等机械</p>
<p>3．平移式传动，最大程度减少铝材擦伤等，提高成品率</p>
<p>4．具体规格尺寸根据挤压机要求确定。</p>
<p>主要性能</p>
<p>1．PLC控制、触摸屏显示与操作</p>
<p>2．成熟制作与控制工艺</p>
<p>3．平移采用齿轮转动，油缸驱动</p>
<p>4．集中式液压站设计</p>
<p>5．平移式毛毡便于维护与更换</p>
<p>6．附带脚踏开关操作，便于各种操作</p>