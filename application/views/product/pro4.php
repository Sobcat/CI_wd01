<h2>交接式双牵引机</h2>
<img src="/pinrui/assets/images/pro/004.png" alt="">

<p>说明：</p>
<p>本设备是挤压机配套生产铝及铝合金型材的关健设备。主要用于牵引、锯切分离、传送挤压成型铝材、棒材。</p>
<p>工作时牵引车上钳口咬着型材前端，并保持与型材一定的拉力，且自动地与挤压机的挤出速度实现同步，把型材从模腔中平直地引出，免得各挤压型材的长度参差不齐，防止型材悬弹扭曲，从而提高型材的成材率和表面质量；</p>
<p>以一定的张力牵引型材，使料在张力下冷却，减少变形，使多根挤压时金属流动均匀，单根料挤压时帮助使模子定径带处金属流动慢的部位加快，整个截面上金属流动均匀，它大致能补偿5％的流出速度。</p>
<p>采用双牵引机可以提高生产效率，双牵引系统不需要依靠非挤压周期去重新安排预计下次的挤压周期，不停机锯切，达到连续挤压的目的。作为一个系统，双牵引机提供更高的产量，同时减少废料。</p>
