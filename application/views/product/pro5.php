<h2>小型牵引机</h2>
<img src="/pinrui/assets/images/pro/005.png" alt="">

<p>技术特点：</p>
<p>小型双牵引机采用两套牵引头和一套同步中断锯构成，牵引夹头采用机械式结构，轻巧灵活，不停机挤压时牵引不需要交接，大大的降低了牵引机故障率，该牵引机适用于挤压小料、薄料、一模多孔比较快速的型材，同时该机在不常要牵引式可单独使用自动中断的功能，大大节省的生产人力和生产的安全性。</p>
<p>根据生产工艺设定力矩（人机界面设定力矩）</p>
<p>牵引机以设定的力矩与挤出型材同步牵引</p>
<p>自动中断方式</p>
<p>钢丝绳循环牵引</p>
<p>跟随型材出料速度，返回最快牵引速度为1米/秒</p>
<p>0KG－80KG牵引力</p>
<p>镰刀式气动或机械钳口夹料</p>
<p>施耐德M258系列 PLC ESTUN伺服控制器。</p>
